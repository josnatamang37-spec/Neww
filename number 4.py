num01 = int(input("Input 1st integer number: "))
num02 = int(input("Input 2nd integer number: "))
num03 = int(input("Input 3rd integer number: "))
sum = num01 + num02 + num03
product = num01 * num02 * num03
average = float(sum) / 3
